<?php
/**
 *    Writes a custom message to the log file for debugging purposes.
 *    The message is prepended with a current timestamp and file identifier.
 *
 *    @param string $info_message The message to write to the log file.    
 *
 */
function log_entry($info_message) { 
    global $config;
    
    if (!$info_message || $info_message == '') {
        return;
    }
       
    error_log('PHP '.$config['app_name'].' Message: ('.$_SERVER['PHP_SELF'].') '.str_replace("\n", "", $info_message));
}



/**
 *    Prettyprints a JSON string.
 *  
 *    Source: umbrae at gmail dot com (http://www.php.net/manual/de/function.json-encode.php#80339)    
 *
 *    @param string $json The compressed JSON data to be beautified.
 *
 */
function json_format($json) 
{ 
    $tab = "  "; 
    $new_json = ""; 
    $indent_level = 0; 
    $in_string = false; 

    $json_obj = json_decode($json); 

    if($json_obj === false) 
        return false; 

    $json = json_encode($json_obj); 
    $len = strlen($json); 

    for($c = 0; $c < $len; $c++) 
    { 
        $char = $json[$c]; 
        switch($char) 
        { 
            case '{': 
            case '[': 
                if(!$in_string) 
                { 
                    $new_json .= $char . "\n" . str_repeat($tab, $indent_level+1); 
                    $indent_level++; 
                } 
                else 
                { 
                    $new_json .= $char; 
                } 
                break; 
            case '}': 
            case ']': 
                if(!$in_string) 
                { 
                    $indent_level--; 
                    $new_json .= "\n" . str_repeat($tab, $indent_level) . $char; 
                } 
                else 
                { 
                    $new_json .= $char; 
                } 
                break; 
            case ',': 
                if(!$in_string) 
                { 
                    $new_json .= ",\n" . str_repeat($tab, $indent_level); 
                } 
                else 
                { 
                    $new_json .= $char; 
                } 
                break; 
            case ':': 
                if(!$in_string) 
                { 
                    $new_json .= ": "; 
                } 
                else 
                { 
                    $new_json .= $char; 
                } 
                break; 
            case '"': 
                if($c > 0 && $json[$c-1] != '\\') 
                { 
                    $in_string = !$in_string; 
                } 
            default: 
                $new_json .= $char; 
                break;                    
        } 
    } 

    return $new_json; 
}
